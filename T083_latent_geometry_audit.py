def run_pca(results):
    """Run PCA on viability+fertility metrics. Returns explained variance and loadings."""
    metric_names = ["C", "P", "G", "R", "S", "SR", "NP", "RC", "RD", "OE"]
    X = np.array([[r["viability"].get(m, r["fertility"].get(m, 0)) for m in metric_names] for r in results])
    X_centered = X - X.mean(axis=0)
    U, s, Vt = np.linalg.svd(X_centered, full_matrices=False)
    eigvals = s ** 2
    explained = eigvals / eigvals.sum() * 100
    cumulative = np.cumsum(explained)
    intrinsic_dim = int(np.searchsorted(cumulative, 90) + 1)
    # Loadings: correlation of each metric with PCs
    loadings = Vt.T
    return {
        "explained": [round(float(e), 2) for e in explained],
        "cumulative": [round(float(c), 2) for c in cumulative],
        "pc1_pct": round(float(explained[0]), 1),
        "pc2_pct": round(float(explained[1]), 1) if len(explained) > 1 else 0,
        "intrinsic_dim": int(intrinsic_dim),
        "n_components": len(explained),
        "loadings_pc1": [round(float(l), 3) for l in loadings[:, 0]],
        "loadings_pc2": [round(float(l), 3) for l in loadings[:, 1]],
        "metric_names": metric_names,
    }


def run_corridor_analysis(results):
    """Analyze fertile corridor geometry."""
    viable = [r for r in results if r["viable"]]
    if len(viable) < 10:
        return {"corridor_detected": None, "edge_of_chaos_pct": None, "n_viable": len(viable)}
    stabilities = np.array([r["stability"] for r in viable])
    fertilities = np.array([r["fertility_index"] for r in viable])
    n_bins = 5
    bin_edges = np.linspace(stabilities.min(), stabilities.max(), n_bins + 1)
    bin_max_fert = []
    for i in range(n_bins):
        mask = (stabilities >= bin_edges[i]) & (stabilities <= bin_edges[i + 1]) if i == n_bins - 1 else \
               (stabilities >= bin_edges[i]) & (stabilities < bin_edges[i + 1])
        bin_max_fert.append(float(np.max(fertilities[mask])) if mask.sum() > 0 else 0.0)
    mid_idx = n_bins // 2
    corridor_detected = bool(bin_max_fert[mid_idx] >= 0.7)
    med_stab = float(np.median(stabilities))
    med_fert = float(np.median(fertilities))
    quads = {"HS/HF": 0, "HS/LF": 0, "LS/HF": 0, "LS/LF": 0}
    for s, f in zip(stabilities, fertilities):
        if s >= med_stab and f >= med_fert:
            quads["HS/HF"] += 1
        elif s >= med_stab:
            quads["HS/LF"] += 1
        elif f >= med_fert:
            quads["LS/HF"] += 1
        else:
            quads["LS/LF"] += 1
    total_q = sum(quads.values())
    edge_of_chaos_pct = round(quads["LS/HF"] / total_q * 100, 1) if total_q else None
    return {
        "corridor_detected": corridor_detected,
        "edge_of_chaos_pct": edge_of_chaos_pct,
        "n_viable": len(viable),
        "stability_range": [round(float(stabilities.min()), 3), round(float(stabilities.max()), 3)],
        "med_stability": float(np.median(stabilities)),
        "quads": quads,
    }


