# EXTENDED SYSTEM — can be augmented with geometry-generating mechanisms
# ============================================================

class ExtendedSystem:
    def __init__(self, params):
        self.params = params

        # Core parameters (same as T081/T082)
        self.base_n = params.get("base_n_states", 4)
        self.determinism = params.get("determinism", 0.7)
        self.connectivity = params.get("connectivity", 0.5)
        self.novelty_drive = params.get("novelty_drive", 0.3)
        self.boundary_strength = params.get("boundary_strength", 0.7)
        self.self_model_level = params.get("self_model_level", 0)
        self.sm_influence = params.get("self_model_influence", 0.0)

        # Geometry-generating mechanism parameters
        self.hierarchy_depth = params.get("hierarchy_depth", 0)
        self.hierarchy_influence = params.get("hierarchy_influence", 0.0)
        self.resource_limit = params.get("resource_limit", 0)
        self.resource_competition = params.get("resource_competition", 0.0)
        self.adaptation_rate = params.get("adaptation_rate", 0.0)
        self.context_sensitivity = params.get("context_sensitivity", 0.0)
        self.corridor_feedback = params.get("corridor_feedback", 0.0)

        # Initialize state space
        self.states = list(range(self.base_n))
        self.n_actual = self.base_n
        self.T = self._build_transition_matrix()

        # Hierarchical structure: assign levels to states
        if self.hierarchy_depth > 0:
            self.state_levels = [random.randint(0, self.hierarchy_depth - 1) for _ in range(self.base_n)]
        else:
            self.state_levels = [0] * self.base_n

        # Resource model
        if self.resource_limit > 0:
            self.resources = {s: random.uniform(0.5, 1.0) for s in range(self.base_n)}
            self.resource_max = {s: 1.0 for s in range(self.base_n)}
        else:
            self.resources = {}
            self.resource_max = {}

        # Adaptation: running statistics of transition outcomes
        if self.adaptation_rate > 0:
            self.transition_counts = Counter()
            self.transition_success = Counter()
        else:
            self.transition_counts = None
            self.transition_success = None

        # Multi-timescale: slow context state
        if self.context_sensitivity > 0:
            self.context_state = 0
            self.context_duration = 0
            self.context_history = [0]
        else:
            self.context_state = None
            self.context_duration = None
            self.context_history = None

        # Corridor feedback: running estimate of stability
        if self.corridor_feedback > 0:
            self.stability_history = []
        else:
            self.stability_history = None

        self.history = []
        self.visit_counts = Counter()
        self.state_ages = {}
        self.fixed_point_reached = False

    def _build_transition_matrix(self):
        n = self.base_n
        T = np.zeros((n, n))
        mask = np.random.random((n, n)) < self.connectivity
        np.fill_diagonal(mask, True)
        raw = np.random.random((n, n)) * mask
        if self.determinism > 0.8:
            for i in range(n):
                if np.random.random() < self.determinism:
                    raw[i] = 0
                    raw[i, np.random.randint(n)] = 1
        row_sums = raw.sum(axis=1)
        row_sums[row_sums == 0] = 1
        T = raw / row_sums[:, np.newaxis]
        return T

    def _hierarchical_modulation(self, current, candidate):
        """Modify transition probabilities based on hierarchical levels."""
        if self.hierarchy_depth <= 0 or self.hierarchy_influence <= 0:
            return candidate
        if current >= len(self.state_levels) or candidate >= len(self.state_levels):
            return candidate
        cur_level = self.state_levels[current]
        cand_level = self.state_levels[candidate]
        # Prefer same-level transitions; penalize cross-level jumps
        if cur_level == cand_level:
            return candidate  # no penalty
        if random.random() < self.hierarchy_influence:
            # Stay at current level — try to find a same-level state
            same_level = [s for s in range(self.n_actual) if s < len(self.state_levels)
                          and self.state_levels[s] == cur_level]
            if same_level:
                return random.choice(same_level)
            # If none, try adjacent level
            adj_level = cur_level + (1 if cand_level > cur_level else -1)
            adj = [s for s in range(self.n_actual) if s < len(self.state_levels)
                   and self.state_levels[s] == adj_level]
            if adj:
                return random.choice(adj)
        return candidate

    def _resource_check(self, state):
        """Check if state has sufficient resources; consume some."""
        if self.resource_limit <= 0 or self.resource_competition <= 0:
            return True
        if state not in self.resources:
            self.resources[state] = 0.5
            self.resource_max[state] = 1.0
        if self.resources[state] <= 0:
            return False
        self.resources[state] -= random.uniform(0, self.resource_competition * 0.1)
        return True

    def _replenish_resources(self):
        """Let resources recover gradually."""
        if self.resource_limit <= 0 or self.resource_competition <= 0:
            return
        for s in self.resources:
            self.resources[s] = min(self.resource_max[s],
                                    self.resources[s] + random.uniform(0, self.resource_competition * 0.05))

    def _adapt_transition(self, from_state, to_state, outcome):
        """Adapt transition probabilities based on outcome quality."""
        if self.adaptation_rate <= 0:
            return
        if self.transition_counts is None:
            return
        key = (from_state, to_state)
        self.transition_counts[key] += 1
        self.transition_success[key] = self.transition_success.get(key, 0) + (1 if outcome > 0.5 else 0)

    def _context_step(self):
        """Update slow context state."""
        if self.context_sensitivity <= 0:
            return
        self.context_duration += 1
        if self.context_duration > random.randint(10, 30):
            self.context_state = random.randint(0, max(1, self.hierarchy_depth - 1))
            self.context_duration = 0
        self.context_history.append(self.context_state)

    def _corridor_adjustment(self, current_cycle_length):
        """Adjust dynamics to stay near intermediate stability."""
        if self.corridor_feedback <= 0:
            return 0.0
        if current_cycle_length <= 1:
            # Too rigid — increase novelty
            return +self.corridor_feedback * 0.3
        elif current_cycle_length >= 10:
            # Too chaotic — decrease novelty
            return -self.corridor_feedback * 0.3
        # In corridor — no adjustment
        return 0.0

    def step(self):
        if not self.history:
            self.history.append(0)
            self.visit_counts[0] += 1
            self.state_ages[0] = 0
            self._context_step()
            self._replenish_resources()
            return 0

        current = self.history[-1]

        # Multi-timescale: context modulates transitions
        effective_T = self.T.copy()
        if self.context_sensitivity > 0 and self.context_state > 0:
            context_level = self.context_state
            # Bias transitions toward or away from certain levels
            for i in range(min(self.n_actual, len(effective_T))):
                for j in range(min(self.n_actual, len(effective_T[i]))):
                    if j < len(self.state_levels) and self.state_levels[j] == context_level:
                        effective_T[i, j] *= (1.0 + self.context_sensitivity * 0.5)
            # Renormalize
            row_sums = effective_T.sum(axis=1)
            row_sums[row_sums == 0] = 1
            effective_T = effective_T / row_sums[:, np.newaxis]

        # Self-modeling
        if self.self_model_level > 0 and np.random.random() < self.sm_influence:
            modulated = self._self_model_modulated(current)
            if modulated != current:
                current = modulated

        # Corridor feedback: adjust effective novelty drive
        cycle_len = self._find_recent_cycle_length()
        drive_adjust = self._corridor_adjustment(cycle_len)
        effective_novelty = max(0.0, min(1.0, self.novelty_drive + drive_adjust))

        # Novelty generation
        if np.random.random() < effective_novelty:
            new_state = self._generate_new_state()
            if new_state is not None:
                self.history.append(new_state)
                self.visit_counts[new_state] += 1
                self.state_ages[new_state] = 0
                if new_state >= len(self.state_levels):
                    if self.hierarchy_depth > 0:
                        self.state_levels.append(random.randint(0, self.hierarchy_depth - 1))
                        self.state_levels[self.base_n - 1] = random.randint(0, self.hierarchy_depth - 1)
                    else:
                        self.state_levels.append(0)
                if self.resource_limit > 0:
                    self.resources[new_state] = 0.8
                    self.resource_max[new_state] = 1.0
                self._context_step()
                self._replenish_resources()
                return new_state

        # Normal transition
        if current < len(effective_T) and self.n_actual > 0:
            next_state = np.random.choice(self.n_actual, p=effective_T[current])
        else:
            next_state = 0

        # Hierarchical modulation
        if self.hierarchy_depth > 0:
            next_state = self._hierarchical_modulation(current, next_state)

        # Resource check
        if self.resource_limit > 0:
            if not self._resource_check(next_state):
                # State exhausted — try another
                candidates = [s for s in range(self.n_actual) if s in self.resources and self.resources[s] > 0]
                if candidates:
                    next_state = random.choice(candidates)

        self.history.append(next_state)
        self.visit_counts[next_state] += 1

        # Adaptive learning
        if self.adaptation_rate > 0:
            outcome = random.random()  # simulated outcome
            self._adapt_transition(current, next_state, outcome)

        self._context_step()
        self._replenish_resources()
        return next_state

    def _self_model_modulated(self, current):
        if self.self_model_level == 1:
            recent = self.history[-min(3, len(self.history)):]
            if len(set(recent)) == 1:
                return random.choice(range(self.n_actual))
        elif self.self_model_level == 2:
            if self.visit_counts[current] > max(self.n_actual, 3):
                candidates = [s for s in range(self.n_actual) if self.visit_counts[s] < self.visit_counts[current]]
                if candidates:
                    return random.choice(candidates)
        elif self.self_model_level >= 3:
            if self._detect_cycle():
                candidates = [s for s in range(self.n_actual) if self.visit_counts.get(s, 0) < 2]
                if candidates:
                    return random.choice(candidates)
        return current

    def _detect_cycle(self):
        if len(self.history) < 6:
            return False
        recent = self.history[-6:]
        return len(recent) == 6 and len(set(recent)) <= 3

    def _find_recent_cycle_length(self):
        h = self.history
        if len(h) < 4:
            return 0
        recent = h[-20:]
        seen = {}
        for i, s in enumerate(recent):
            if s in seen:
                return i - seen[s]
            seen[s] = i
        return len(set(h))

    def _generate_new_state(self):
        if self.boundary_strength > 0.9:
            return None
        expansion_ok = np.random.random() < (1.0 - self.boundary_strength) * 0.3
        if not expansion_ok:
            return None
        new_id = self.n_actual
        self.n_actual += 1
        self.states.append(new_id)
        old_T = self.T
        new_T = np.zeros((self.n_actual, self.n_actual))
        new_T[:old_T.shape[0], :old_T.shape[0]] = old_T
        for i in range(self.n_actual):
            conn = np.random.random(self.n_actual) < self.connectivity
            conn[i] = True
            raw = np.random.random(self.n_actual) * conn
            if raw.sum() == 0:
                raw[i] = 1
            new_T[i] = raw / raw.sum()
        self.T = new_T
        self.visit_counts[new_id] = 0
        self.state_ages[new_id] = 0
        if new_id >= len(self.state_levels):
            if self.hierarchy_depth > 0:
                self.state_levels.append(random.randint(0, self.hierarchy_depth - 1))
            else:
                self.state_levels.append(0)
        return new_id

    def run(self, steps=200):
        for _ in range(steps):
            self.step()
        return self._compute_metrics()

    def _compute_metrics(self):
        h = self.history
        n = len(h)
        if n == 0:
            return defaultdict(float)
        unique_ratio = len(set(h)) / max(1, self.n_actual)
        cycle_length = self._find_recent_cycle_length()
        convergence = 1.0 if cycle_length <= 1 else (1.0 / cycle_length if cycle_length > 0 else 0)
        entropy = self._compute_entropy()
        self_correlation = self._compute_self_correlation()
        trans_div = self._compute_transition_diversity()
        sm_ratio = self.self_model_level / 3.0
        sm_impact = self.sm_influence
        return {
            "unique_ratio": unique_ratio,
            "cycle_length": cycle_length,
            "convergence": convergence,
            "entropy": entropy,
            "self_correlation": self_correlation,
            "transition_diversity": trans_div,
            "sm_ratio": sm_ratio,
            "sm_impact": sm_impact,
            "n_states_actual": self.n_actual,
            "total_steps": n,
            "state_expansions": self.n_actual - self.base_n,
        }

    def _find_cycle_length(self):
        h = self.history
        if len(h) < 4:
            return len(set(h))
        recent = h[-20:]
        seen = {}
        for i, s in enumerate(recent):
            if s in seen:
                return i - seen[s]
            seen[s] = i
        return len(set(h))

    def _compute_entropy(self):
        if not self.history:
            return 0
        counts = Counter(self.history)
        total = len(self.history)
        ent = 0
        for c in counts.values():
            p = c / total
            if p > 0:
                ent -= p * math.log2(p)
        return ent / max(1, math.log2(len(counts)))

    def _compute_self_correlation(self):
        h = self.history
        if len(h) < 4:
            return 0
        lag = min(3, len(h) // 2)
        matches = sum(1 for i in range(len(h) - lag) if h[i] == h[i + lag])
        return matches / (len(h) - lag)

    def _compute_transition_diversity(self):
        trans = list(zip(self.history[:-1], self.history[1:]))
        if not trans:
            return 0
        return len(set(trans)) / max(1, len(trans))


